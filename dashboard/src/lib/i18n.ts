const LOCALE_NAMES: Record<Locale, string> = {
  en: 'English',
  'zh-TW': '繁體中文',
  'zh-CN': '简体中文',
  ja: '日本語',
  ko: '한국어',
  pt: 'Português',
  fr: 'Français',
  de: 'Deutsch',
  vi: 'Tiếng Việt',
  es: 'Español',
  th: 'ภาษาไทย',
};

let currentLocale: Locale = 'en';

function detectLocale(): Locale {
  // 1. Check localStorage
  const saved = localStorage.getItem('memesh-locale') as Locale;
  if (saved && translations[saved]) return saved;

  // 2. Check browser language
  const lang = navigator.language;
  if (lang.startsWith('zh-TW') || lang.startsWith('zh-Hant')) return 'zh-TW';
  if (lang.startsWith('zh')) return 'zh-CN';
  const short = lang.split('-')[0] as Locale;
  if (translations[short]) return short;

  return 'en';
}

export function initLocale(): Locale {
  currentLocale = detectLocale();
  return currentLocale;
}

export function setLocale(locale: Locale): void {
  currentLocale = locale;
  localStorage.setItem('memesh-locale', locale);
}

export function t(key: string): string {
  return translations[currentLocale]?.[key] || translations.en[key] || key;
}

export function getLocale(): Locale {
  return currentLocale;
}

export function getLocales(): Array<{ code: Locale; name: string }> {
  return Object.entries(LOCALE_NAMES).map(([code, name]) => ({ code: code as Locale, name }));
}

export type { Locale };
